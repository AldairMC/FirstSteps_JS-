var typecard = "Credic card";

switch (typecard) {
    case "Credic card":
        console.log("Tienes una tarjeta de credito");
        break;

    case "Debit card":
        console.log("Tienes una tarjeta de debito");
        
    default:
    console.log("No tienes una tarjeta");
        break;
}